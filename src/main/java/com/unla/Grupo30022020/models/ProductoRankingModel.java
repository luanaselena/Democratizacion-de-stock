package com.unla.Grupo30022020.models;

public class ProductoRankingModel {

	private ProductoModel producto;
	
	private int cantidad;

	public ProductoRankingModel() {

	}
	
	public ProductoRankingModel(ProductoModel producto, int cantidad) {
		this.producto = producto;
		this.cantidad = cantidad;
	}

	public ProductoModel getProducto() {
		return producto;
	}

	public void setProducto(ProductoModel producto) {
		this.producto = producto;
	}

	public int getCantidad() {
		return cantidad;
	}

	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + cantidad;
		result = prime * result + ((producto == null) ? 0 : producto.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ProductoRankingModel other = (ProductoRankingModel) obj;
		if (cantidad != other.cantidad)
			return false;
		if (producto == null) {
			if (other.producto != null)
				return false;
		} else if (!producto.equals(other.producto))
			return false;
		return true;
	}
	
}
